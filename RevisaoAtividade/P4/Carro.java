package P4;

public class Carro extends Veiculo{
    private int numPortas;
    private int capPortamalas;
    private int numPassage;

    public void setNumPortas(int numPortas) {
        this.numPortas = numPortas;
    }
    public int getNumPortas() {
        return numPortas;
    }

    public void setCapPortamalas(int capPortamalas) {
        this.capPortamalas = capPortamalas;
    }
    public int getCapPortamalas() {
        return capPortamalas;
    }
    
    public void setNumPassage(int numPassage) {
        
        this.numPassage = numPassage;
    }
    public int getNumPassage() {
        return numPassage;
    }
    
}
